package com.example.parcial.entidades

// Clase que representa un viaje
class Viajes {
    var id: Int = 0 // ID del viaje, usado como identificador único
    var Destino: String? = null // Lugar de destino del viaje
    var Fecha_Inicio: String? = null // Fecha de inicio del viaje
    var Fecha_Fin: String? = null // Fecha de finalización del viaje
    var Actividades: String? = null // Actividades planificadas durante el viaje
    var Lugares: String? = null // Lugares a visitar durante el viaje
}
